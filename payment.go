// Pagos stores the data of the payment receipts complement ("Complemento para recepción
// de pagos" 2.0) added to CFDI documents of type "P".
type Pagos struct {
	XMLName xml.Name `xml:"pago20:Pagos"`

	// Version is the version of the payment complement. Fixed to "2.0".
	Version string `xml:",attr"`

	// Totales reports the total amount of the payments and their taxes, expressed in
	// MXN.
	Totales *PagosTotales `xml:"pago20:Totales"`
	// Pago lists the payments received.
	Pago []*Pago `xml:"pago20:Pago"`
}

// PagosTotales stores the total amounts of the payments and the taxes that derive from
// them, expressed in MXN.
type PagosTotales struct {
	// TotalRetencionesIVA is the total of the IVA taxes withheld in the payments.
	TotalRetencionesIVA string `xml:",attr,omitempty"`
	// TotalRetencionesISR is the total of the ISR taxes withheld in the payments.
	TotalRetencionesISR string `xml:",attr,omitempty"`
	// TotalRetencionesIEPS is the total of the IEPS taxes withheld in the payments.
	TotalRetencionesIEPS string `xml:",attr,omitempty"`
	// TotalTrasladosBaseIVA16 is the total base of the IVA transferred at the 16% rate.
	TotalTrasladosBaseIVA16 string `xml:",attr,omitempty"`
	// TotalTrasladosImpuestoIVA16 is the total of the IVA taxes transferred at the 16% rate.
	TotalTrasladosImpuestoIVA16 string `xml:",attr,omitempty"`
	// TotalTrasladosBaseIVA8 is the total base of the IVA transferred at the 8% rate.
	TotalTrasladosBaseIVA8 string `xml:",attr,omitempty"`
	// TotalTrasladosImpuestoIVA8 is the total of the IVA taxes transferred at the 8% rate.
	TotalTrasladosImpuestoIVA8 string `xml:",attr,omitempty"`
	// TotalTrasladosBaseIVA0 is the total base of the IVA transferred at the 0% rate.
	TotalTrasladosBaseIVA0 string `xml:",attr,omitempty"`
	// TotalTrasladosImpuestoIVA0 is the total of the IVA taxes transferred at the 0% rate.
	TotalTrasladosImpuestoIVA0 string `xml:",attr,omitempty"`
	// TotalTrasladosBaseIVAExento is the total base of the IVA transferred exempt.
	TotalTrasladosBaseIVAExento string `xml:",attr,omitempty"`
	// MontoTotalPagos is the total of the payments reported in the Pago nodes.
	MontoTotalPagos string `xml:",attr"`
}

// Pago stores the data of a single payment received.
type Pago struct {
	// FechaPago is the date and time when the beneficiary receives the payment
	// (aaaa-mm-ddThh:mm:ss, ISO 8601; 12:00:00 when the time is unknown).
	FechaPago string `xml:",attr"`
	// FormaDePagoP is the key of the means by which the payment is made (c_FormaPago).
	FormaDePagoP string `xml:",attr"`
	// MonedaP is the key of the currency used to make the payment (ISO 4217).
	MonedaP string `xml:",attr"`
	// TipoCambioP is the exchange rate to MXN at the date of the payment. Required
	// when MonedaP is different from MXN.
	TipoCambioP string `xml:",attr,omitempty"`
	// Monto is the amount of the payment.
	Monto string `xml:",attr"`
	// NumOperacion is the cheque, authorisation, reference, SPEI tracking key or
	// analogous number that identifies the payment operation.
	NumOperacion string `xml:",attr,omitempty"`
	// RfcEmisorCtaOrd is the RFC of the entity issuing the ordering account.
	RfcEmisorCtaOrd string `xml:",attr,omitempty"`
	// NomBancoOrdExt is the name of the ordering bank, required when foreign.
	NomBancoOrdExt string `xml:",attr,omitempty"`
	// CtaOrdenante is the number of the account with which the payment was made.
	CtaOrdenante string `xml:",attr,omitempty"`
	// RfcEmisorCtaBen is the RFC of the entity operating the beneficiary account.
	RfcEmisorCtaBen string `xml:",attr,omitempty"`
	// CtaBeneficiario is the number of the account where the payment was received.
	CtaBeneficiario string `xml:",attr,omitempty"`
	// TipoCadPago is the key of the type of payment chain generated by the entity
	// receiving the payment (c_TipoCadenaPago).
	TipoCadPago string `xml:",attr,omitempty"`
	// CertPago is the certificate covering the payment, in base 64. Required when
	// TipoCadPago is set.
	CertPago string `xml:",attr,omitempty"`
	// CadPago is the original string of the payment receipt generated by the entity
	// issuing the beneficiary account. Required when TipoCadPago is set.
	CadPago string `xml:",attr,omitempty"`
	// SelloPago is the digital seal associated with the payment, in base 64. Required
	// when TipoCadPago is set.
	SelloPago string `xml:",attr,omitempty"`

	// DoctoRelacionado lists the documents related to the payment.
	DoctoRelacionado []*DoctoRelacionado `xml:"pago20:DoctoRelacionado"`
	// ImpuestosP summarises the taxes applicable to the amount of the payment
	// received, expressed in the payment currency.
	ImpuestosP *ImpuestosP `xml:"pago20:ImpuestosP,omitempty"`
}

// DoctoRelacionado stores the data of a document related to a payment.
type DoctoRelacionado struct {
	// IDDocumento is the identifier of the related document: the fiscal folio (UUID)
	// of an electronic invoice or the operation number of a digital document.
	IDDocumento string `xml:"IdDocumento,attr"`
	// Serie is the series of the related document, for internal control.
	Serie string `xml:",attr,omitempty"`
	// Folio is the folio of the related document, for internal control.
	Folio string `xml:",attr,omitempty"`
	// MonedaDR is the key of the currency of the amounts of the related document (ISO
	// 4217).
	MonedaDR string `xml:",attr"`
	// EquivalenciaDR is the number of units of the currency of the related document
	// that are equivalent to one unit of the payment currency. Required when the
	// currencies differ.
	EquivalenciaDR string `xml:",attr,omitempty"`
	// NumParcialidad is the number of the instalment that the payment corresponds to.
	NumParcialidad string `xml:",attr"`
	// ImpSaldoAnt is the outstanding balance of the previous instalment, or the total
	// amount of the related document for the first instalment.
	ImpSaldoAnt string `xml:",attr"`
	// ImpPagado is the amount paid for the related document.
	ImpPagado string `xml:",attr"`
	// ImpSaldoInsoluto is the difference between the previous balance and the amount
	// paid.
	ImpSaldoInsoluto string `xml:",attr"`
	// ObjetoImpDR expresses whether the payment of the related document is subject to
	// tax or not (c_ObjetoImp).
	ObjetoImpDR string `xml:",attr"`

	// ImpuestosDR reports the taxes applicable to the amount of the payment received,
	// expressed in the currency of the related document.
	ImpuestosDR *ImpuestosDR `xml:"pago20:ImpuestosDR,omitempty"`
}

